                                                    <p class="justificar_texto" style="color: red;"><i><strong>Objectivos</strong></i></p>
                                                    <p class="justificar_texto">
                                                        Erradicar definitivamente o analfabetismo nos jovens e adultos com mais de 15 anos, propiciando uma educação de qualidade, em articulação com actividades geradoras de rendimento, como forma de facilitar a sua inserção no mercado de trabalho.
                                                    </p>
                                                    <p class="justificar_texto" style="color: red;"><i><strong>Estratégias</strong></i></p>

                                                    <p>

                                                        <b>Estratégia nº 1:</b> Aumentar e melhorar o acesso à educação de jovens e adultos

                                                        <br></p><p>

                                                    <h5><b>Acções Prioritárias</b></h5><p><ol><ol><ol><li>

                                                                    Expandir, equipar e dinamizar de maneira equitativa centros de formação destinados à Educação para Jovens e Adultos (DEJA);

                                                                </li><li>

                                                                    Desencadear uma campanha de sensibilização e mobilização geral contra o analfabetismo;

                                                                </li><li>

                                                                    Estimular a procura da Educação de Jovens e Adultos em zonas mais difíceis e desfavorecidas, através de campanhas de sensibilização e estímulos compensatórios;

                                                                </li><li>

                                                                    Apoiar e consolidar as iniciativas extra estatais, nomeadamente das associações comunitárias, ONGs e demais parceiros em direcção da Educação de Jovens e Adultos;

                                                                </li><li>

                                                                    Garantir a retenção dos formados, em particular as mulheres, através de estímulos compensatórios;

                                                                </li><li>

                                                                    Mobilizar o apoio de todos os parceiros nacionais e internacionais para a consecução do objectivo de erradicação de analfabetismo.</li></ol></ol></ol><b>Estratégia nº 2:</b> Melhorar a qualidade e eficiência da formação e da aprendizagem&nbsp;<br></p><p>

                                                    <h5>Acções Prioritárias</h5></p></p><p><p><p><ol><ol><li>Formar e capacitar educadores, sup?ervisores e formadores;</li><li>Garantir a produção e a distribuição de manuais e guias de apoio pedagógico;&nbsp;</li><li>Reforçar a função de seguimento e avaliação de desempenho;&nbsp;</li><li>Lutar contra o abandono, através de sensibilização, melhoria de condições físicas dos centros, atractividade de métodos de aprendizagem, estímulos compensatórios, entre outros;&nbsp;</li><li>Combater o retorno ao analfabetismo através da criação de programas adequados e implementação de medidas que facilitem a inserção sócio profissional;&nbsp;</li><li>Criar e institucionalizar um sistema de educação alternativa ao ensino formal,&nbsp;&nbsp;adequando os actuais cursos nocturnos a essa modalidade de forma a garantir um sistema  educativo alternativo mais adaptado às características da população alvo.</li></ol></ol></p><b><p>&nbsp;Estratégia nº 3:&nbsp;<span style="font-weight: 400;">Reforçar capacidades institucionais da DEJA</span></p></b><b><p><p></p></p></b><p>

                                                    <h5>Acções Prioritárias</h5><p><ol><ol><li>Dotar a Direcção da Educação de Jovens e Adultos de condições adequadas de trabalho, essencialmente, instalações físicas, meios de transporte, equipamentos móveis e imóveis;</li><li>Dispensar módulos de formação para os diferentes escalões dos quadros da DEJA;&nbsp;</li><li>Dispensar seminários de formação destinados ao pessoal da DEJA.</li></ol></ol></p></p><b><p><p><br>Estratégia nº 4:<b></b><span style="font-weight: 400;"> Adoptar uma política apropriada de inserção profissional dos formados&nbsp;</span><p></p></p><p><span style="font-weight: 400;">

                                                                <h5>Acções Prioritárias</h5></span></p></p></b></p></p><p><p style="display: inline !important;"><span style="font-weight: 400;"></span></p></p><p style="display: inline !important;"></p><p><span style="font-weight: 400;"></span></p><p><b></b></p><h5><b style="font-size: 14px;"><p></p><p></p></b><ol><li>?<span style="font-weight: 400; font-size: 14px;">Dinamizar e direccionar, cada vez mais, os cursos de alfabetização para actividades profissionais potênciadoras de inserção dos alfabetizados na vida social e profissional;</span></li><li><p><span style="font-weight: 400; font-size: 14px;">

                                                                        Criar e fazer funcionar centros de formação académica e profissional para adultos (mulheres e homens);&nbsp;</span></p></li></ol></h5>
