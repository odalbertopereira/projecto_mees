                                                    <p class="justificar_texto" style="color: red;"><i><strong>Objectivos</strong></i></p>
                                                    <p class="justificar_texto">
                                                        Propiciar a todas as crianças Santomenses, incluindo aquelas  com NEE, acesso a um Ensino Básico de 6 anos, de boa qualidade, capaz de facilitar a sua continuidade no Ensino Secundário, e que o terminem em condições de eficácia aceitável.
                                                    </p>
                                                    <p class="justificar_texto" style="color: red;"><i><strong>Estratégias</strong></i></p>
                                                    <p>

                                                        <b>Estratégia nº 1:</b> Consolidar os ganhos obtidos no domínio de acesso ao Ensino Básico:&nbsp;<br>
                                                    </p>
                                                    <p>
                                                    <p>Melhorar e apetrechar a rede escolar de forma equitativa e sustentável, construindo, lá onde for necessário, e equipando salas de aulas, sobretudo nas zonas menos favorecidas, com vista a diminuir as assimetrias distritais, e garantir a continuidade educativa na escola (a problemática equação do Acesso/Distância/Permanência de alunos nas escolas); </p>

                                                    <p>
                                                    <p>Garantir que todas as escolas primárias ofereçam o ciclo completo de 1ª a 6ª classe até 2015, o que facilita a continuidade educativa dos alunos na escola, contribuindo, de maneira, na redução da taxa de abandono; actualmente, apenas 17% de estabelecimentos do EB dispensam os dois ciclos completos (1ª à 6ª classes); sabe-se que no ano lectivo 2009/2010, 86,9% dos alunos que terminaram a 4ª classe não tinham a possibilidade de continuar a 5ª classe no mesmo estabelecimento de ensino, devendo ou abandonar a escola ou mudar de estabelecimento;
                                                    </p>
                                                    <p>
                                                    <p>Dinamizar a politica de transporte escolar e encontrar mecanismos capazes de atenuar o fardo de gestão deste empreendimento; &nbsp;</p>

                                                    <p>

                                                    <p>Consolidar o investimento do Estado no Ensino Básico injectando fundos suplementares para garantir as melhores condições de aprendizagens nas escolas;</p>
                                                    <p>
                                                        <b>Estratégia nº 2:</b> Melhorar a qualidade e a eficiência do ensino e da aprendizagem
                                                    </p>
                                                    <p><p><p>
                                                    <h5><b>Acções Prioritárias</b></h5>
                                                    <p><p>
                                                    <ul>
                                                        <blockquote>
                                                            <li>
                                                                Formar e capacitar professores, como forma de vencer o desafio da qualidade do ensino, o que passa pela definição e implementação de uma politica clara e assumida de formação inicial e formação continua do corpo docente e pela transformação da actual EFOPE numa Escola ou Instituto Superior de Educação, adaptando o seu currículo e o seu funcionamento às reais necessidades do país e aos desafios da qualidade que se colocam;
                                                            </li>
                                                            <li>
                                                                Garantir a atractividade da docência, enquanto profissão, o que passa pela promoção de progressão na carreira docente, instauração de uma grelha salarial realmente atraente, a adopção de um sistema de avaliação que preveja a atribuição de prémios de desempenho, formação e capacitação continua, instituição de um concurso de admissão, criação de condições condignas de trabalho, proclamar ?Dia do Professor?, etc. <br>
                                                            </li>
                                                            <li>
                                                                Prosseguir e consolidar o processo de produção e distribuição de manuais e outros equipamentos escolares;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Introduzir o ensino de uma língua estrangeira no 1º ciclo do ensino básico e uma segunda no 2º ciclo, de preferência o inglês;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Estender o tempo de aprendizagem no Ensino Básico, passando das actuais 17,5 horas semanais para 25 horas, num primeiro tempo, (válido para os dois ciclos do EB);<br></li><li>
                                                                Criar e implementar um dispositivo de luta contra o insucesso e o abandono escolar, o que passa pelo reforço do enquadramento pedagógico de professores e de inspectores, pela formação específica de professores nas técnicas e métodos de avaliação e pela implementação de um sistema de apoio aos alunos com dificuldades, entre outros;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Criar um fundo de apoio à inovação destinado às escolas primárias que apresentem excelente desempenho nessa área;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Criar e implementar um mecanismo de captação de excelências a nível nacional através de concursos, instituição de quadro de honra e outras formas de estímulo, e assegurar um enquadramento adequado para os melhores seleccionados;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Estabelecer o exame de aferição no fim da 6ª classe que permita certificar as competências adquiridas;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Atingir o rácio de 30 alunos/sala, contra 79 actualmente;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Criar as condições para uma total assumpção, por parte do Estado, dos custos ligados à alimentação escolar à partir de 2015;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Desenvolver as actividades extra-escolares e desportivas em colaboração com as comunidades locais, através da criação e dinamização de Centros de Desenvolvimento Pessoal e Social, (que inculcam valores da cidadania); Centros de Recursos e de Apoio (que fornecem apoio pedagógico e orientação sócio-profissional); Centros de Aconselhamento e Escuta (que dispensam apoios psicológicos e instruem no aspecto de SSR); Centros desportivos (que apoiam e encorajam a prática e o desenvolvimento do desporto escolar), entre outros;
                                                                <br>
                                                            </li>
                                                            <li>
                                                                Incentivar o movimento associativo de pais e encarregados de educação, como parceiro essencial do governo na gestão física e moral das escolas, particularmente, na educação moral dos seus educandos, na manutenção das infra-estruturas escolares e na manutenção e gestão disciplinar das escola.</li></blockquote></ul>
                                                    <b>Estratégia nº 3:</b> Garantir a igualdade de oportunidade e de sucesso escolar dos alunos, segundo o seu meio de residência, nível de vida das famílias e o sexo (válido para todos os ciclos)&nbsp;</p><p>&nbsp; <b></b><b>Acções Prioritárias</b></p><p><p><p><ol><ol><li>Aplicar a politica de discriminação positiva em relação às zonas e categorias sociais mais desfavorecidas e vulneráveis, pela: i) atribuição de subsídios de isolamento e outros estímulos, nomeadamente a subvenção de meios de transporte público ou pessoal (tipo motorizada) para os professores afectos às zonas mais desfavorecidas; ii) adopção de medidas de apoio sócio-educativo destinadas às crianças oriundas de zonas e famílias carenciadas, nomeadamente a atribuição de bolsas de estudo, a promoção da saúde escolar, o melhoramento da alimentação escolar, a atribuição de subsídios de transporte, a subvenção de uniforme escolar, etc.;<b>

                                                                    <br></b></li><li>Racionalizar critérios e aplicar a justiça distributiva na afectação de professores e dos demais benefícios da educação, evitando que os melhores sejam afectos às zonas mais favorecidas; sabe-se que em 2004/2005, 54,7% de professores com  formação especifica leccionavam no distrito de Agua Grande e 24,8% no Mê-Zochi contra 1,8% e 1,1%, nos distritos e Lemba e Caué respectivamente;<b>

                                                                    <br></b></li><li>Estandardizar as normas de construção e equipamento de edifícios escolares para todas as zonas;<b>

                                                                    <br></b></li><li>Desencadear uma campanha de sensibilização sobre a escolarização das raparigas das zonas mais carenciadas;

                                                            </li>
                                                            <li>

                                                                Atribuir estímulos, nomeadamente bolsas de estudos, para as crianças oriundas de zonas e categorias sociais mais desfavorecidas e vulneráveis.

                                                            </li>
                                                        </ol>
                                                    </ol>
